def login_required(view):
    @functools.wraps(view)
    def wrapped_view(**kwargs):
        if g.user is None:
            return redirect(url_for('auth.login'))

        return view(**kwargs)
    #kwargs is used because we don't know how many key word args we will have for a given function

    return wrapped_view